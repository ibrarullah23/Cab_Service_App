    private void cmbDropLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbDropLocationActionPerformed
        // TODO add your handling code here:
        if(cmbDropLocation.getSelectedIndex() >0){
            int pickup = value.get(cmbPickLocation.getSelectedIndex()-1);
            int dropoff = value.get(cmbDropLocation.getSelectedIndex()-1);
            int d = Math.abs(pickup-dropoff);
            txtDistance.setText(new DecimalFormat().format(d));
        } else{
            txtDistance.setText("0");
        }
    }//GEN-LAST:event_cmbDropLocationActionPerformed

    private void cmbVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbVehicleActionPerformed
        // TODO add your handling code here:

        int distance = Integer.parseInt(txtDistance.getText());
        switch(cmbVehicle.getSelectedIndex()){
            case 0:

            break;
            case 1:
            TotalFare =  60 + 9*distance;
            break;
            case 2:
            TotalFare =  80 + 13*distance;
            break;
            case 3:
            TotalFare =  106 + 15*distance;
            break;
            case 4:
            TotalFare =  120 + 20*distance;
            break;
            case 5:
            TotalFare =  135 + 23*distance;
            break;
        }

        txtFare.setText("Rs : " + new DecimalFormat().format(TotalFare));

    }//GEN-LAST:event_cmbVehicleActionPerformed

