    private boolean validateFields(){
        
        if(cmbPickLocation.getSelectedIndex() == 0){
            getToolkit().beep();
            cmbPickLocation.requestFocus();
        } else if(cmbDropLocation.getSelectedIndex() == 0){
            getToolkit().beep();
            cmbDropLocation.requestFocus();
        } else if(cmbVehicle.getSelectedIndex() == 0){
            getToolkit().beep();
            cmbVehicle.requestFocus();
        } else{
            return true;
        }
        
        return false;
    }
